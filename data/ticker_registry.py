class TickerRegistry:
   
    @classmethod
    def get_ticker_name(cls, ticker: str) -> str:
        """
        核心查找逻辑：
        1. 清理输入代码（去除 sz/sh 前缀）
        2. 先从 hs300 找，再从 cyb100 找
        3. 找不到则返回 'Unknown'
        """
        # 处理带前缀的情况，如 'sz000001' -> '000001'
        clean_code = ticker.replace("sz", "").replace("sh", "")
        
        # 链式查找
        name = hs300.get(clean_code) or cyb100.get(clean_code)
        
        return name if name else f"Unknown({ticker})"

